# Программа для расчета интенсивности света
# на заданном расстоянии от источника света

def calculate_intensity(distance, power):
    """
    Функция для расчета интенсивности света
    на заданном расстоянии от источника света.

    Аргументы:
    distance -- расстояние от источника света (в метрах)
    power -- мощность источника света (в ваттах)

    Возвращает:
    intensity -- интенсивность света (в канделах)
    """
    intensity = power / (4 * 3.1415 * distance ** 2)
    return intensity


# Пример использования функции
distance = 10  # метров
power = 100  # ватт
intensity = calculate_intensity(distance, power)
print(f"Интенсивность света на расстоянии {distance} метров: {intensity} кд")
